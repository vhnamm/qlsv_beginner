    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String name = txtName.getText();
            String msv = txtMSV.getText();
            String lop = txtClass.getText();
            double gpa = Double.valueOf(txtGPA.getText());

            Student a = new Student(msv, name, lop, gpa);
            a.chuanHoa(name, lop, msv);
            StudentDAO stuDAO = new StudentDAO();
            if(stuDAO.addData(a)){

                JOptionPane.showMessageDialog(rootPane, "Them sinh vien thanh cong", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


